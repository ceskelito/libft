#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*last;
	char	cc;

	cc = (char) c;
	last = NULL;
	while (*s)
	{
		if (*s == cc)
			last = (char *)s;
		s++;
	}
	if (cc == '\0')
		return ((char *)s);
	return (last);
}
